import Foundation
import UIKit
extension UIColor{
   class func lpsGrayTextColor() -> UIColor {
        return RGB(130, G: 130, B: 130)
    }
    class func lpsThemColor() -> UIColor {
        return RGB(77, G: 139, B: 217)
    }
    class func lpsGrayLineColor() -> UIColor {
        return RGB(230, G: 230, B: 230)
    }
    class func lpsGrayBgColor() -> UIColor {
        return RGB(246, G: 246, B: 246)
    }
    class func lpsBlackTextColor() -> UIColor {
        return RGB(0, G: 0, B: 0)
    }
    
    
}
